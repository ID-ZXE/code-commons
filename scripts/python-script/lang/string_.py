message = "Hello World"
print(message)
# 读取字符串index位置的元素
print(message[0])
# 单引号, 双引号
message2 = "'Hello World'"
print(message2)
# 换行
print()
# 首字母大写
print(message.title())
# 输出大写与小写
print(message.upper())
print(message.lower())
# 字符串拼接
first_name = "zhang"
last_name = "hang"
print(first_name + last_name)

message3 = " python "
# 去除两端空格
print(message3.strip())
# 去除right侧的空格
print(message3.rstrip())
# 去除left侧的空格
print(message3.lstrip())
